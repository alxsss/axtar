			if ($this->isNew() ) {
				$this->modifiedColumns[] = EventStatusCommentPeer::ID;
			}

						if ($this->isModified()) {
				if ($this->isNew()) {
					$pk = EventStatusCommentPeer::doInsert($this, $con);
